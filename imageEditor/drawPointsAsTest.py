import cv2
import numpy as np

def draw_circles(coordinates, image_size=(900, 766), circle_radius=5, color=(0, 0, 255), thickness=-1):
    # Create a blank image
    image = np.zeros((image_size[1], image_size[0], 3), dtype=np.uint8)

    # Draw circles at each coordinate
    for coord in coordinates:
        x, y = coord
        # Adjust the y-coordinate to match the top-left origin
        adjusted_y = image_size[1] - y
        adjusted_coord = (x, adjusted_y)
        cv2.circle(image, adjusted_coord, circle_radius, color, thickness)

    # Display the image
    cv2.imshow('Image with Circles', image)
    cv2.waitKey(0)
    cv2.destroyAllWindows()

